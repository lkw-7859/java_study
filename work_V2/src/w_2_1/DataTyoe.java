package w_2_1;

public class DataTyoe {
	
	public static void main(String[] args) {
		// 정수형
		byte b = 1;
		short s = 2;
		int i = 3;
		long l = 4;	
		
		//실수형
		float f = 10.2f;
		double d = 10.3;
		
		//문자형
		char c = 'A';
		
		//논리형
		boolean bool = true;
		
		//문자열
		String str = "ABCD";
		
		System.out.println("정수형 b = " + b);
		System.out.println("정수형 s = " + s);
		System.out.println("정수형 i = " + i);
		System.out.println("실수형 f = " + f);
		System.out.println("실수형 d = " + d);
		System.out.println("논리형 bool = " + bool);
		System.out.println("문자형 c = " + c);
		System.out.println("문자열형 str = " + str);
	}
}
